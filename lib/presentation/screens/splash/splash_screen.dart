    with SingleTickerProviderStateMixin {
  @override
  void initState() {
    super.initState();
    Future.delayed(const Duration(seconds: 3), () {
      // Placeholder: later navigate to onboarding or home
    });
  }

  @override
  Widget build(BuildContext context) {
    return Scaffold(
      backgroundColor: Colors.white,
      body: Center(
        child: Lottie.asset(
          'assets/lottie/fitmotion_loading.json',
          width: 200,
          height: 200,
          repeat: true,
        ),
      ),
    );
  }
}
